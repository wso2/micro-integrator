package org.wso2.micro.integrator.business.messaging.hl7.common.data.conf;

/**
 * This class holds BAM server connection details
 */
public class ServerConfig {

    public static final String URL_SEPARATOR = ",";

    private String secureUrl;

    private String url;

    private String username;

    private String password;

    public ServerConfig(String secureUrl, String url, String username, String password) {
        this.secureUrl = secureUrl;
        this.url = url;
        this.username = username;
        this.password = password;
    }

    public String getSecureUrl() {
        return secureUrl;
    }

    public void setSecureUrl(String secureUrl) {
        this.secureUrl = secureUrl;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public boolean isLoadBalancingConfig() {
        return (url != null && url.contains(URL_SEPARATOR));
    }
}
